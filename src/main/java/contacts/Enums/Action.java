package contacts.Enums;

/**
 * Supported main application actions.
 */
public enum Action {
    /** Action to add a new contact. */
    ADD,
    /** Action to list all existing contacts. */
    LIST,
    /** Action to search through contacts. */
    SEARCH,
    /** Action to count the total number of contacts. */
    COUNT,
    /** Action to exit the application. */
    EXIT
}
